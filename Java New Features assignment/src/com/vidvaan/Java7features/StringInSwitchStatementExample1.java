package com.vidvaan.Java7features;

public class StringInSwitchStatementExample1 {
	public static void main(String[] args) {
		String game = "Hockey";
		switch (game) {
		case "Hockey":
			System.out.println("Let's play Hockey");
			break;
		case "Cricket":
			System.out.println("Let's play Cricket");
			break;
		case "Football":
			System.out.println("Let's play Football");
		}
	}
}
